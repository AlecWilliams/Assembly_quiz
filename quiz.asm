	.ORIG x3000
	
START	AND R2,R2,#0	;CLEAR R2
	LD R0, Q1	;LOAD Q1
	PUTS		;PRINT IT
	JSR GET_ANSW	;JUMP TO SUBROUTINE 
	LD R1, Q1_ANSW
	JSR GET_PNTS
		

	LD R0, Q2	;LOAD Q2
	PUTS		;PRINT IT
	JSR GET_ANSW	;SUB
	LD R1,Q2_ANSW
	JSR GET_PNTS

	LD R0, Q3	;LOAD Q3
	PUTS		;PRINT
	JSR GET_ANSW	;SUB
	LD R1,Q3_ANSW
	JSR GET_PNTS


	JSR GET_RES
	PUTS
	;HERE I WOULD PRINT RESULTS
	;AND R3,R3,#0
	;AND R2,R2,#0
	;AND R1,R1,#0
	BRnzp START
	HALT

Q1	.FILL x3300
Q1_ANSW	.FILL x3357
Q2	.FILL x335B
Q2_ANSW	.FILL x33BB
Q3	.FILL x33BF
Q3_ANSW	.FILL x3442

ANSW	.STRINGZ "Answer: "
RS_1	.FILL x3446
RS_2	.FILL x3467
RS_3	.FILL x3488
RS_4	.FILL x34B2

SAVE_R0	.FILL #0
SAVE_R1	.FILL #0
SAVE_R7	.FILL #0

;USED TO DETERMINE FINAL SCORE
SC_1	.FILL #-26
SC_2	.FILL #-18
SC_3	.FILL #-12
SC_4	.FILL #-6

SAVE_R72	.FILL #0
ASCII	.FILL x-30

;========GET ANSWER============
;
;
GET_ANSW

	ST R1, SAVE_R1	;SAVE R1
	ST R7, SAVE_R7	;SAVE R7
	
	GETC		;GET THE CHARACTER
	ST R0, SAVE_R0	;SAVE R0

	JSR PRINT	;PRINT ANSWER	

	
	LD R1, ASCII	;LOAD #-30 INTO R1
	ADD R0,R0,R1	;MAKE R0 THE ACTUAL NUMBER
	
	;JSR GET_PNTS	;SUBROUTINE FOR POINTS

	LD R7, SAVE_R7
	LD R1, SAVE_R1
	RET


;==========GET POINTS====================
;
;
GET_PNTS
	
	ADD R0,R0,#-1	;ADD -1 TO ANSWER TO GET THE LOCATION RIGHT
	

	;LD R1, Q1_ANSW	;LOAD ADRESS OF SCORE
	ADD R1,R0,R1	;ADD THE # TO GET RIGHT SCORE
	
	LDR R3,R1,#0	;KEEP SCORE IN R3!
	ADD R2,R2,R3	;RUNNING SCORE
	ADD R0,R0,#1	;ADD 1 TO GET ORIGINAL ANSWER	

        RET

;==========PRINT=============
PRINT	LEA R0,ANSW
	ST R7, SAVE_R72
	PUTS
	LD R0,SAVE_R0

	OUT
	LD R7, SAVE_R72
	RET
	
;==============GET RESULTS=================
GET_RES
	AND R0,R0,#0	;CLEAR R0
	ADD R0,R2,#0	;COPY R2
	LD R1, SC_1	;LOAD #-26 INTO R1
	ADD R0,R1,R0	;CHECK SCORE
	
	BRzp	DONE_1

	AND R0,R0,#0
	ADD R0,R2,#0
	LD R1, SC_2	;LOAD SCORE 2
	ADD R0,R1,R0
	
	BRzp	DONE_2

	AND R0,R0,#0
	ADD R0,R2,#0
	LD R1, SC_2
	ADD R0,R1,R0
	
	BRzp	DONE_3
	
	LD R0, RS_1
	RET
	
DONE_1	LD R0, RS_4
	RET

DONE_2	LD R0,RS_3
	RET

DONE_3	LD R0,RS_2
	RET
	
	
	.END

	
